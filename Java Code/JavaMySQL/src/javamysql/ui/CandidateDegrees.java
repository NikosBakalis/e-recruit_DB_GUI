    public CandidateDegrees getCandidateDegrees(String candidateUsername) {
        iCRUDImpl.openConnection();
        try{
            CandidateDegrees candidateDegrees = new CandidateDegrees();
            String query = "SELECT * FROM has_degree WHERE cand_usrname = '" + candidateUsername + "'";
            PreparedStatement preparedStatement = iCRUDImpl.getConnection().prepareStatement(query);
            ResultSet resultSet = preparedStatement.executeQuery();
            TableDegrees.setModel(DbUtils.resultSetToTableModel(resultSet));
            return candidateDegrees;
        } catch (SQLException e) {
            return null;
        }
    }
    
