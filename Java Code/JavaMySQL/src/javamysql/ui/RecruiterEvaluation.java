    public Evaluation evaluation(int jobID){
        iCRUDImpl.openConnection();
        String query = "{CALL evaluation(?)}";
        Evaluation evaluation = new Evaluation();
        try {
            CallableStatement callableStatement = iCRUDImpl.getConnection().prepareCall(query);
            callableStatement.setInt(1, jobID);
            callableStatement.execute();
            ResultSet resultSet1, resultSet2, resultSet3;
            resultSet1 = callableStatement.getResultSet();
            TableMessage.setModel(DbUtils.resultSetToTableModel(resultSet1));
            resultSet1.close();
            if(callableStatement.getMoreResults()){
                resultSet2 = callableStatement.getResultSet();
                TableScore.setModel(DbUtils.resultSetToTableModel(resultSet2));
                resultSet2.close();
            }
            if(callableStatement.getMoreResults()){
                resultSet3 = callableStatement.getResultSet();
                TableExplanation.setModel(DbUtils.resultSetToTableModel(resultSet3));
                resultSet3.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ICRUDImpl.class.getName()).log(Level.SEVERE, null, ex);
        }
        return evaluation;
    }

