    public AdminHistory getAdminHistory() {
        iCRUDImpl.openConnection();
        try{
            AdminHistory getAdminHistory = new AdminHistory();
            String query = "SELECT * FROM history";
            PreparedStatement preparedStatement = iCRUDImpl.getConnection().prepareStatement(query);
            ResultSet resultSet = preparedStatement.executeQuery();
            TableHistory.setModel(DbUtils.resultSetToTableModel(resultSet));
            return getAdminHistory;
        } catch (SQLException e) {
            return null;
        }
    }

