    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        DefaultTableModel table = (DefaultTableModel)TableHistory.getModel();
        String value = this.search.getText();
        TableRowSorter<DefaultTableModel> tableRowSorter = new TableRowSorter<>(table);
        TableHistory.setRowSorter(tableRowSorter);
        tableRowSorter.setRowFilter(RowFilter.regexFilter(value));
    }//GEN-LAST:event_searchKeyReleased

